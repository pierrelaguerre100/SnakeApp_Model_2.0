USE SNAKEGAME;

CREATE TABLE USERS (
USER_ID INT PRIMARY KEY AUTO_INCREMENT,
USERNAME VARCHAR(50),
PASSWORD VARCHAR(50),
DATE_REGISTERED DATETIME
);

CREATE TABLE SCORES (
SCORE_ID INT PRIMARY KEY AUTO_INCREMENT,
USER_ID INT, 
SCORE INT,
LEVEL INT,
DIFFICULTY VARCHAR(20),
DATE_PLAYED DATETIME,
DURATION_SEC INT,
FOREIGN KEY (USER_ID) REFERENCES USERS(USER_ID)
);

